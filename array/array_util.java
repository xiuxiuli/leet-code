package array;

public class array_util {

    public static void main(String[] args){
        int[] nums = {1, 2, 3, 4, 5};
        System.out.println("数组长度: " + nums.length);
        System.out.println("第一个元素: " + nums[0]);
        System.out.println("最后一个元素: " + nums[nums.length - 1]);
    }
    
}
